export type EvidenceSourceType = 'ai_monitor' | 'dast' | 'security_scan' | 'tls_scan' | 'config_scan' | 'manual_upload' | 'system_log';
export type EvidenceStatus = 'fresh' | 'expired' | 'expiring_soon';

export interface Evidence {
  id: string;
  evidenceId: string; // e.g., "EV-001", "ev-004"
  name: string;
  description?: string;
  
  // Source
  source: EvidenceSource;
  sourceType: EvidenceSourceType;
  sourceName: string;
  
  // Control mapping
  controlIds: string[];
  controlNames: string[];
  frameworkIds: string[];
  frameworkNames: string[];
  
  // Status
  status: EvidenceStatus;
  validityPeriod?: number; // Days until expiration
  expiresAt?: string; // ISO date
  
  // Metadata
  createdAt: string;
  createdBy?: string;
  uploadedBy?: string;
  fileType?: string;
  fileSize?: number;
  
  // File/Content
  fileUrl?: string;
  previewUrl?: string;
  content?: string;
  
  // Tags/Categories
  tags?: string[];
  category?: string;
  
  // Audit info
  auditLocked?: boolean;
  auditId?: string;
}

