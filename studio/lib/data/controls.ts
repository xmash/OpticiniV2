export type ControlStatus = 'pass' | 'fail' | 'partial' | 'not_evaluated';
export type ControlSeverity = 'critical' | 'high' | 'medium' | 'low';
export type ControlType = 'preventive' | 'detective' | 'corrective';
export type EvaluationMethod = 'automated' | 'manual' | 'hybrid';
export type ControlFrequency = 'continuous' | 'daily' | 'weekly' | 'monthly' | 'annually';

export interface Control {
  id: string;
  controlId: string; // e.g., "SOC2-CC6.1", "ISO27001-A.9.1.1"
  name: string;
  description: string;
  
  // Framework mapping
  frameworks: string[]; // Framework IDs
  frameworkNames: string[]; // Display names
  
  // Status
  status: ControlStatus;
  severity: ControlSeverity;
  
  // Evaluation
  lastEvaluated?: string; // ISO date
  evaluatedBy?: string; // User/system
  evaluationMethod: EvaluationMethod;
  
  // Failure details
  failureReason?: string;
  failingAssets?: string[];
  failingCount?: number;
  
  // Evidence
  evidenceCount: number;
  evidenceIds: string[];
  
  // Metrics
  uptimePercentage?: number;
  timeOutOfCompliance?: number; // minutes
  
  // Recommendations
  fixRecommendations?: string[];
  relatedControls?: string[];
  
  // Metadata
  category?: string;
  controlType: ControlType;
  frequency: ControlFrequency;
}

