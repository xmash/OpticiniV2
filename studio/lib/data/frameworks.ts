export type FrameworkCategory = 'security' | 'privacy' | 'industry' | 'regional';
export type FrameworkStatus = 'ready' | 'in_progress' | 'at_risk' | 'not_started';

export interface Control {
  id: string;
  control_id: string;
  name: string;
  status: 'pass' | 'fail' | 'partial' | 'not_evaluated';
  severity: 'critical' | 'high' | 'medium' | 'low';
  category?: string;
  control_type?: string;
  evaluation_method?: string;
  last_evaluated?: string;
}

export interface Framework {
  id: string;
  name: string;
  code: string;
  category: FrameworkCategory;
  description: string;
  icon?: string; // Lucide icon name
  
  // Status
  enabled: boolean;
  status: FrameworkStatus;
  
  // Metrics
  complianceScore: number; // 0-100
  totalControls: number;
  passingControls: number;
  failingControls: number;
  notEvaluatedControls: number;
  
  // Metadata
  lastEvaluated?: string; // ISO date
  nextAuditDate?: string; // ISO date
  
  // Nested relationships (from API)
  controls?: Control[]; // Controls belonging to this framework
}

